    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
       ProdutoDAO dao = new ProdutoDAO();
        Produto produtoAdd = new Produto();
        if (!flag) {
            produtoAdd.setNome(campoProduto.getText());
            produtoAdd.setPreco(Double.valueOf(campoPreco.getText()));
            dao.add(produtoAdd);
            JOptionPane.showMessageDialog(null, "Produto cadastrado com sucesso.", "Ok", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        } else {
            produto.setNome(campoProduto.getText());
            produto.setPreco(Double.valueOf(campoPreco.getText()));
            dao.update(produto);
            JOptionPane.showMessageDialog(null, "Produto editado com sucesso.", "Ok", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed

