    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        ClienteDAO dao = new ClienteDAO();
        Endereco endereco = new Endereco();
        Cliente clienteAdd = new Cliente();
        if (!flag) {
            clienteAdd.setNome(campoNome.getText());
            clienteAdd.setCartao(Long.valueOf(campoCartao.getText()));
            clienteAdd.setCpf(campoCpf.getText());
            endereco.setBairro(campoBairro.getText());
            endereco.setCidade(campoBairro.getText());
            endereco.setEstado(campoEstado.getText());
            endereco.setNumero(campoNumero.getText());
            endereco.setRua(campoRua.getText());
            clienteAdd.setEndereco(endereco);
            dao.add(clienteAdd);
            JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso.", "Ok", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        } else {
            cliente.setNome(campoNome.getText());
            cliente.setCartao(Long.valueOf(campoCartao.getText()));
            cliente.setCpf(campoCpf.getText());
            endereco.setBairro(campoBairro.getText());
            endereco.setCidade(campoBairro.getText());
            endereco.setEstado(campoEstado.getText());
            endereco.setNumero(campoNumero.getText());
            endereco.setRua(campoRua.getText());
            cliente.setEndereco(endereco);
            dao.update(cliente);
            JOptionPane.showMessageDialog(null, "Cliente editado com sucesso.", "Ok", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed

